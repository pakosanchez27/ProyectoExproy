)

CREATE TABLE PAGO (
  ID_PAGO INT PRIMARY KEY AUTO_INCREMENT,
  ID_SUBSCRIPCION INT(11),
  FECHA_PAGO DATE,
  COSTO DECIMAL(10, 2),
  FOREIGN KEY (ID_SUBSCRIPCION) REFERENCES SUBSCRIPCION (ID_SUBSCRIPCION)
);

CREATE TABLE MENSAJE (
  ID_MENSAJE INT PRIMARY KEY AUTO_INCREMENT,
