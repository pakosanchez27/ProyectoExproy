CREATE TABLE EMPRESA (
  ID_EMPRESA INT(11) PRIMARY KEY AUTO_INCREMENT,
  EMP_NOMBRE VARCHAR(255),
  EMP_APELLIDO VARCHAR(255),
  EMP_TELEFONO VARCHAR(255),
  EMP_EMPRESA VARCHAR(255),
  EMP_FOTOPERFIL VARCHAR(255),
  EMP_FOTOPORTADA VARCHAR(255),
  EMP_GENERO VARCHAR(255),
  EMP_ACERCA TEXT,
  EMP_NACIMIENTO DATE,
  EMP_CARGO VARCHAR(255),
  EMP_URL VARCHAR(255),
  EMP_FOTORECLUTADOR VARCHAR(255),
  ID_USUARIO INT(11),
  FOREIGN KEY (ID_USUARIO) REFERENCES USUARIO (ID_USUARIO)
);

CREATE TABLE VACANTE (
  ID_VACANTE INT(11) PRIMARY KEY AUTO_INCREMENT,
  TITULO VARCHAR(255),
  DESCRIPCION TEXT,
  SALARIO DECIMAL(10, 2),
  CIUDAD VARCHAR(255),
  ESTADO VARCHAR(255),
  AREA VARCHAR(255),
  PUESTO VARCHAR(255),
  EDUCACION VARCHAR(255),
  TIPO_CONTRATO VARCHAR(255),
  HORARIO VARCHAR(255),
  MODO_TRABAJO VARCHAR(255),
  VENCIMIENTO DATE,
  NUMERO_VACANTES INT(11),
  ID_EMPRESA INT(11),
  FOREIGN KEY (ID_EMPRESA) REFERENCES EMPRESA (ID_EMPRESA)
);

CREATE TABLE EMPRESA_VACANTE (
  ID_EMPRESA INT(11),
  ID_VACANTE INT(11),
  FECHA_CREACION_VACANTE DATE,
  PRIMARY KEY (ID_EMPRESA, ID_VACANTE),
  FOREIGN KEY (ID_EMPRESA) REFERENCES EMPRESA (ID_EMPRESA),
  FOREIGN KEY (ID_VACANTE) REFERENCES VACANTE (ID_VACANTE)
);

CREATE TABLE POSTULACION (
  ID_POSTULACION INT(11) PRIMARY KEY AUTO_INCREMENT,
  ID_CANDIDATO INT(11),
  ID_VACANTE INT(11),
  FECHA_POSTULACION DATE,
  ESTADO VARCHAR(255),
  FOREIGN KEY (ID_CANDIDATO) REFERENCES CANDIDATO (ID_CANDIDATO),
  FOREIGN KEY (ID_VACANTE) REFERENCES VACANTE (ID_VACANTE)
);

CREATE TABLE SUBSCRIPCION (
  ID_SUBSCRIPCION INT PRIMARY KEY AUTO_INCREMENT,
  ID_EMPRESA INT(11),
  FECHA_INICIO DATE,
  FECHA_VENCIMIENTO DATE,
  TIPO_SUSCRIPCION VARCHAR(255),
  ESTADO_SUSCRIPCION VARCHAR(255),
  COSTO DECIMAL(10, 2),
  FOREIGN KEY (ID_EMPRESA) REFERENCES EMPRESA (ID_EMPRESA)
);

CREATE TABLE CITA (
  ID_CITA INT PRIMARY KEY AUTO_INCREMENT,
  ID_CANDIDATO INT(11),
  ID_EMPRESA INT(11),
  FECHA_CITA DATE,
  HORA_CITA TIME,
  FOREIGN KEY (ID_CANDIDATO) REFERENCES CANDIDATO (ID_CANDIDATO),
  FOREIGN KEY (ID_EMPRESA) REFERENCES EMPRESA (ID_EMPRESA)
)

CREATE TABLE DOCUMENTOS(
  ID_DOCUMENTO INT PRIMARY KEY AUTO_INCREMENT,
  ID_USUARIO INT(11),
  NOMBRE_DOCUMENTO VARCHAR(255),
